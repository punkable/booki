let lang = "es";

export function setLang(value) {
  if (value === "en" || value === "es") {
    lang = value;
  } else {
    const n = (typeof navigator !== "undefined" ? navigator.language : "es") || "es";
    lang = n.toLowerCase().startsWith("en") ? "en" : "es";
  }
}

export function t(key) {
  return (DICT[lang] && DICT[lang][key]) || DICT.es[key] || key;
}

export function curLang() {
  return lang;
}
