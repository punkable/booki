let currentLang = "en";

export function setLanguage(lang) {
  if (LOCALES[lang]) currentLang = lang;
}

export function getLanguage() {
  return currentLang;
}

export function getAvailableLanguages() {
  return Object.entries(LOCALES).map(([code, _]) => ({
    code,
    name: code === "en" ? "English" : "Español"
  }));
}

export function t(key, fallback = "") {
  const locale = LOCALES[currentLang];
  return locale?.[key] ?? LOCALES.en[key] ?? (fallback || key);
}

export function detectBrowserLanguage() {
  const navLang = (navigator.language || navigator.userLanguage || "en").slice(0, 2);
  const supported = Object.keys(LOCALES).includes(navLang) ? navLang : "en";
  const stored = localStorage.getItem("booki-lang");
  if (stored && LOCALES[stored]) return stored;
  return supported;
}
